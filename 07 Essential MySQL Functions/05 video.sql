-- Calculation on DATES

SELECT DATE_ADD(NOW(), INTERVAL 1 DAY);
SELECT DATE_ADD(NOW(), INTERVAL 1 YEAR);
SELECT DATE_ADD(NOW(), INTERVAL -1 MONTH);
SELECT DATE_SUB(NOW(), INTERVAL 1 MONTH);

SELECT ROUND(DATEDIFF('2025-10-04','1995-10-04') / 365);
SELECT TIMESTAMPDIFF(YEAR, '1995-10-04', '2025-10-04') AS years_difference;

SELECT TIME_TO_SEC('21:50');

SELECT TIME_TO_SEC('21:50') - TIME_TO_SEC('13:50');

